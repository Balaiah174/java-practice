
 class AssinmentOperator {
	public static void main(String[] args) {
		int a = 10;
// add
		a += 10;
        System.out.println(a);
        //sub
        a-=9;
		System.out.println(a);
		// mul
		a*=5;
		System.out.println(a);
		//div 
		a/=3;
		System.out.println(a);
		//mod 
		a%=6;
		System.out.println(a);
	}
}
